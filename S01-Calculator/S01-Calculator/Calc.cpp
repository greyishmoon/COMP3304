double Calc::add(double num1, double num2)
{
	return num1 + num2;
}

double Calc::subtract(double num1, double num2)
{
	return num1 - num2;
}

double Calc::multiply(double num1, double num2)
{
	return num1 * num2;
}

double Calc::divide(double num1, double num2)
{
	return num1 / num2;
}
